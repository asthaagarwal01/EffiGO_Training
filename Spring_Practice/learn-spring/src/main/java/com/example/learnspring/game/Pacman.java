package com.example.learnspring.game;

public class Pacman implements GamingConsole{
	
	public void up()
	{
		System.out.println("Pacman Up pressed");
	}
	
	public void down()
	{
		System.out.println("Pacman Down pressed");
	}
	
	public void left()
	{
		System.out.println("Pacman Left pressed");
	}
	
	public void right()
	{
		System.out.println("Pacman Right pressed");
	}

}
